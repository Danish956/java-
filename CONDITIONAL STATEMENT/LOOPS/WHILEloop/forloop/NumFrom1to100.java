
package LOOPS.WHILEloop.forloop;

import java.util.Scanner;

public class NumFrom1to100 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("enter");
        int j = sc.nextInt();
        for(int i = 1; i<=j; i++){
            System.out.println(i);
        }
    }
    
}
